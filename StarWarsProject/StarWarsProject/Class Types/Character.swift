import Foundation

class Character
{
    var name: String?
    var birthYear: String
    var home: String
    var height: Double
    var eyeColor: String
    var hairColor: String
    
    init(name: String, birthYear: String, home: String, height: Double, eyeColor: String, hairColor: String)
    {
        self.name = name
        self.birthYear = birthYear
        self.home = home
        self.height = height
        self.eyeColor = eyeColor
        self.hairColor = hairColor
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
}






























