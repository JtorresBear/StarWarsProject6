import Foundation

class Vehicle {
    var name: String
    var make: String
    var cost: Int
    var length: Double
    var vehicleClass: String
    var crew: Double
    
    init(name: String, make: String, cost: Int, length: Double, vehicleClass: String, crew: Double){
        self.name = name
        self.make = make
        self.cost = cost
        self.length = length
        self.vehicleClass = vehicleClass
        self.crew = crew
    }
    
    
}














